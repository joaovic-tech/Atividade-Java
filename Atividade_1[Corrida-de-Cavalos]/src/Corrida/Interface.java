    private void btnRespostaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRespostaActionPerformed

        
        btnResposta.setVisible(false);

        String resposta1 = getAlternativa.getText();
        if(resposta1.equals(perguntaSorteada.getResposta())){
            getAlternativa.setText("");
            Point p = Cavalo1.getLocation();
            p.x = p.x + 190;
            Cavalo1.setLocation(p);
            resultadoLb.setText("Resposta certa !!!");
            setTotalRespondidas(getTotalRespondidas() + 1);
            setTotalAcertos(getTotalAcertos() + 1);

        }else{
            resultadoLb.setText("Resposta errada !!!");
            setTotalRespondidas(getTotalRespondidas() + 1);
            
            Point p = Cavalo2.getLocation();
            p.x = p.x + 190;
            Cavalo2.setLocation(p);

        }
        
        if(getTotalAcertos() > 3 ){
            JOptionPane.showMessageDialog(null,"Parab√©ns voce ganhou!!!!");
        }
       
    }//GEN-LAST:event_btnRespostaActionPerformed

    private void btnStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStartActionPerformed

        Perguntas jogador1 = new Perguntas();

        Random aleatorio = new Random();

        btnStart.setText("Proxima pergunta");

        int sorteado = 1 + aleatorio.nextInt(20);

        perguntaSorteada = (GeradorP)jogador1.hashPerguntas.get(sorteado);

        perguntaLb.setText(perguntaSorteada.getEnumciado());
        alternativaLb.setText(perguntaSorteada.getAlternativa());

        informativoLb.setText("Digite a alternativa escolhida: ");
        btnResposta.setVisible(true);
        informativoLb.setVisible(true);
        getAlternativa.setVisible(true);
        resultadoLb.setVisible(true);
        scoreLb.setVisible(true);
        resultadoLb.setText(" ");
        scoreLb.setText(" ");
    }//GEN-LAST:event_btnStartActionPerformed

    
    
